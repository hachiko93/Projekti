    private void srediFormu(int konstanta) {
        jbtnPosalji.setEnabled(true);
        jtxtIskoriscenaSlova.setText(null);
        String rec;
        int pokusaji;
        try {
            TransferObjekatZahtev toZahtev = new TransferObjekatZahtev();
            toZahtev.setOperacija(konstanta);
            Komunikacija.getInstance().posaljiZahtev(toZahtev);
            TransferObjekatOdgovor toOdgovor = Komunikacija.getInstance().primiOdgovor();
            if (toOdgovor.getIzuzetak() == null) {
                rec = toOdgovor.getRezultat().toString();
                pokusaji = rec.length() + 8 - brojRazmaka(rec);

                jtxtRec.setText(rec);
                jtxtPokusaji.setText(pokusaji + "");
            } else {
                JOptionPane.showMessageDialog(this, toOdgovor.getPoruka());
            }
        } catch (Exception e) {
        }
    }

    private int brojRazmaka(String rec) {
        int broj = 0;
        char[] recNiz = rec.toCharArray();
        for (char c : recNiz) {
            if (Character.isWhitespace(c)) {
                broj++;
            }
        }
        return broj;
    }

    private void zameniZvezdicu(LinkedList<Integer> i, char slovo) throws Exception {
        char[] recNiz = jtxtRec.getText().trim().toCharArray();
        for (Integer j : i) {
            recNiz[j] = slovo;
        }
        jtxtRec.setText(new String(recNiz));
        proveriDaLiJeKraj(recNiz);
    }

    private void proveriBrojPokusaja() throws Exception {
        int broj = Integer.parseInt(jtxtPokusaji.getText().trim());
        if (broj == 0) {
            TransferObjekatZahtev toZahtev = new TransferObjekatZahtev();
            toZahtev.setOperacija(Konstante.KRAJ_IGRE);
            Komunikacija.getInstance().posaljiZahtev(toZahtev);
            TransferObjekatOdgovor toOdgovor = Komunikacija.getInstance().primiOdgovor();
            if (toOdgovor.getIzuzetak() == null) {
                String rec = toOdgovor.getRezultat().toString();
                jtxtRec.setText(rec);
                jbtnPosalji.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(this, toOdgovor.getPoruka());
            }
            JOptionPane.showMessageDialog(this, "Igra je zavrsena");
        }
    }

    private void proveriDaLiJeKraj(char[] recNiz) throws Exception {
        boolean zvezdice = false;
        for (int i = 0; i < recNiz.length; i++) {
            if (recNiz[i] == '*') {
                zvezdice = true;
            }
        }
        if (!zvezdice) {
            jbtnPosalji.setEnabled(false);
            throw new Exception("Kraj igre! Cestitamo! Pobedili ste");
        }
    }
