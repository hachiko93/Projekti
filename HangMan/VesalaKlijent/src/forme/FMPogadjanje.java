    private void jbtnPosaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnPosaljiActionPerformed
        try {
            String slovo = jtxtSlovo.getText().trim().toUpperCase();
            if (slovo.isEmpty()) {
                throw new Exception("Morate upisati neko slovo");
            }
            if (slovo.length() > 1) {
                throw new Exception("Morate uneti samo po jedno slovo");
            }
            TransferObjekatZahtev toZahtev = new TransferObjekatZahtev();
            toZahtev.setOperacija(Konstante.PROBAJ_SLOVO);
            toZahtev.setParametar(slovo);
            Komunikacija.getInstance().posaljiZahtev(toZahtev);
            TransferObjekatOdgovor toOdgovor = Komunikacija.getInstance().primiOdgovor();
            if (toOdgovor.getIzuzetak() == null) {
                if (toOdgovor.getRezultat() == null) {
                    if (!jtxtIskoriscenaSlova.getText().contains(slovo)) {
                        jtxtIskoriscenaSlova.setText(jtxtIskoriscenaSlova.getText() + " " + slovo);
                    }
                    jtxtPokusaji.setText((Integer.parseInt(jtxtPokusaji.getText().trim()) - 1) + "");
                    proveriBrojPokusaja();
                } else {
                    zameniZvezdicu((LinkedList<Integer>) toOdgovor.getRezultat(), slovo.toCharArray()[0]);
                }
            } else {
                JOptionPane.showMessageDialog(this, toOdgovor.getPoruka());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_jbtnPosaljiActionPerformed

